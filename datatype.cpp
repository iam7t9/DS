#include<iostream>

using namespace std;

int main()
{
    int a;
    float b;
    char c;
    bool d;

    // cout << "size of int, float, char, bool are respectively " << sizeof(a) << sizeof(b) << sizeof(c) << sizeof(d) << endl;
    cout << "int : " << sizeof(a) << endl;
    cout << "float : " << sizeof(b) << endl;
    cout << "char : " << sizeof(c) << endl;
    cout << "bool : " << sizeof(d) << endl;
    
    return 0;
}
